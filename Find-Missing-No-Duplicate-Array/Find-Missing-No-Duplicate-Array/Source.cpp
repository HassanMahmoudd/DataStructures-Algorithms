int findMissingNum(vector<int> array1, vector<int> array2)
{
	int result = 0;
	for (int i = 0; i < array1.size(); i++)
		result = result ^ array1[i];
	for (int i = 0; i < array2.size(); i++)
		result = result ^ array2[i];
	return result;
}

